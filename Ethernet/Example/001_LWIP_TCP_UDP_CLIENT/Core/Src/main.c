/* USER CODE BEGIN 4 */
void tcp_server_task(void){

	addr_size = sizeof(struct sockaddr_in);

	struct sockaddr_in my_addr, their_addr;
	my_addr.sin_addr.s_addr = INADDR_ANY;
	my_addr.sin_family = AF_INET;
	my_addr.sin_port = htons(MYPORT);

	while(1){
		if((sockfd = socket(AF_INET, SOCK_STREAM, 0)) == -1){
	        perror("socket");
	        break;
		}
		if(bind(sockfd,(struct sockaddr *)&my_addr, sizeof(struct sockaddr)) == -1){
			perror("bind");
			break;
		}
		if(listen(sockfd, BACKLOG) == -1){
			perror("listen");
			break;
		}
		if((new_fd = accept(sockfd,(struct sockaddr *)&their_addr, (socklen_t *)&addr_size)) == -1){
			perror("accept");
			break;
		}
		while(1){
			if((bytes = recv(new_fd, &buffer, sizeof(buffer), 0)) == -1){
				perror("receive");
				continue;
			}
			if((int)buffer%3 == 1){
				HAL_GPIO_WritePin(GPIOB, GPIO_PIN_0, GPIO_PIN_SET);
				HAL_GPIO_WritePin(GPIOB, GPIO_PIN_7 | GPIO_PIN_14, GPIO_PIN_RESET);
			}
			else if((int)buffer%3 == 2){
				HAL_GPIO_WritePin(GPIOB, GPIO_PIN_7, GPIO_PIN_SET);
				HAL_GPIO_WritePin(GPIOB, GPIO_PIN_0 | GPIO_PIN_14, GPIO_PIN_RESET);
			}
			else if((int)buffer%3 == 0){
				HAL_GPIO_WritePin(GPIOB, GPIO_PIN_14, GPIO_PIN_SET);
				HAL_GPIO_WritePin(GPIOB, GPIO_PIN_0 | GPIO_PIN_7, GPIO_PIN_RESET);
			}
			if(send(new_fd, &result, sizeof(result), 0) == -1){
				perror("send");
				continue;
			}
		}
		close(new_fd);
		close(sockfd);
	}
}

int sendall(int s, char *buf, int *len)
{
    int total = 0;        // gönderdigimiz byte miktari
    int bytesleft = *len; // eksik kalan byte miktari
    int n;

    while(total < *len) {
        n = send(s, buf+total, bytesleft, 0);
        if (n == -1) { break; }
        total += n;
        bytesleft -= n;
    }

    *len = total; // gönderilen toplam bayt miktarý

    return n==-1?-1:0; // sorun varsa -1, iþlem tamamsa 0 döner
}
